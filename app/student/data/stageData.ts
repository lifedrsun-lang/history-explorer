export const getStageInfo = (
  stage: number
) => {

  const current =
    STAGE_DATA.find(
      (s) => s.id === Number(stage)
    );

  // 기본값
  if (!current) {

    return {
      title: "탐험 시작",

      current: {
        id: 0,
        short: "준비중",
        emoji: "❓",
        lesson: 1,
        era: "기본",
      },

      stages: [
        {
          id: 0,
          short: "준비중",
          emoji: "❓",
        },
      ],
    };

  }

  const stages = STAGE_DATA.filter(
    (s) => s.era === current.era
  );

  return {
    title: current.title,
    current,
    stages,
  };

};